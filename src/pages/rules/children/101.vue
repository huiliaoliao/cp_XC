<b><p id="1">一、玩法类型及承销</p></b>
是一种小盘玩法游戏，属数字型彩票范畴，由中国福利彩票发行管理中心统一组织发行，在全国范围内销售。

<br><br>
<b><p id="2">二、开奖与购买方式</p></b>
1. 本站游戏每天进1期，开奖时间为20:30，开奖号码通过摇奖方式产生，开奖节目将在中国教育电视台一频道和中国福彩网进行直播，播出时间为每日21:15（不含周六）。中彩网、新浪网、搜狐网将每日进行开奖网络直播。购买者可透过本站进行投注，在对其选定的投注号码进行投注；或返点投注，返点越高投注赔率则低。
<br>
2. 购买者可选择机选号码投注、自选号码投注。机选号码投注是指由投注机随机产生投注号码进行投注，自选号码投注是指将购买者选定的号码输入投注机进行投注。
<br>
3. 购买者还可进行多倍投注、追号投注。多倍投注是指同样的投注号码进行翻倍数额购买的投注。追号投注指将一注或一组号码进行两期或两期以上的投注。追号可分为连续追号和间隔追号，连续追号指追号的期数是连续的，间隔追号指追号的期数不连续。
<br>
4. 如果用户投注成功后，若因销售终端故障、通讯线路故障和投注站信用额度受限等原因造成当期无法开奖的，应退还购买者投注金额。

<br>
<br>
<b><p id="3">三、兑奖</p></b>
1. 返奖：无论大小奖均返还至用户在本站的账户中，一旦用户中奖，系统将自动返还中奖金额。可继续投注或提款，永无弃奖。

